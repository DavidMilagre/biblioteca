package milagre.biblioteca;


public class Cadastro {
    private int id;
    private String nome;
    private String cpf;
    private String email;
    private String telefone;
    private String login;
    private String senha;

    // Construtor
    public Cadastro(int id, String nome, String cpf, String email, String telefone, String login, String senha) {
        this.id = id;
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.telefone = telefone;
        this.login = login;
        this.senha = senha;
    }



}
